    void show1() {

        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cafamanagement", "root", "nclc123");
            pst = con.prepareStatement("select * from categorytable");
            ResultSet rs = pst.executeQuery();
            for (int i = mo.getRowCount() - 1; i >= 0; i--) {
                mo.removeRow(i);
            }
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getInt(1));
                v.add(rs.getString(2));
                mo.addRow(v);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManageCategory.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

